export default function darkStyle(tab) {

  tab.style.border = "thin solid #91B7F3";
  tab.style.borderWidth = "3px 5px";
  tab.style.borderRadius = "10px";
  tab.style.backgroundColor = "#2F3135";
  tab.style.color = "white";
  tab.style.width = "400px";
  tab.style.marginInline = "auto";

  return tab;
}