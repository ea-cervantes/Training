// function to style all the text boxes like pokemon ruby
export default function tableStyle(tab) {

  tab.style.border = "thin solid #92C6F4";
  tab.style.borderWidth = "3px 5px";
  tab.style.borderRadius = "10px";
  tab.style.backgroundColor = "white";
  tab.style.color = "black";
  tab.style.width = "400px";
  tab.style.marginInline = "auto";

  return tab;
}